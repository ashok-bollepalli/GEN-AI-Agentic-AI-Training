# Numerical Literals
age = 25
student_id = 101

print(age)
print(student_id)

# Floating literals
price = 99.99
percentage = 15.25
print(price)
print(percentage)

# String literals
name = 'Ashok'
city = "HYD"
msg = """
        I like python
        Python is very easy
"""

#Boolean literals
is_updated = False
is_active = True

# List literals (Mutable)
students = ["Ashok", "John", "Steve", "Anil"]
print(students)

# Tuple literals (Immutable)
courses = ("JAVA", "Python", "GEN AI")
print(courses)

# Set Literals (No Duplicates Allowed)
cities = {"Hyd", "Pune", "Chennai", "Hyd"}
print(cities)

# dictionary literal (Key-Value pairs)
student = {
    "id" : 101,
    "name" : "Ashok",
    "city" : "HYD"
}
print(student)


x = None
print(x)